package fireDrill;

public class QuestionOne {
    public static void main(String[] args) {
        //Prints number One to ten using forLoop

        for(int counter = 1; counter <= 10; counter++){
            System.out.println(counter);
        }

        //using a whileLoop
        int index = 0;
        while (index <= 10){
            System.out.println(index);
            index++;
        }

        }
    }